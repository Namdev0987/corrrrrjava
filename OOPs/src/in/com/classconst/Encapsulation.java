package in.com.classconst;

public class Encapsulation {
	private  int Empid ;  // data hiding
	private int phone ; // data hiding
	
	public void setEmpId(int Empid) {
		this.Empid = Empid;
	}
	
	public int getEmpId() {
		return Empid;
	}
	
	public void setPhone(int phone) {
		this.phone = phone;
	}
	
	public int getPhone() {
		return phone;
	}
	
	
	
}
