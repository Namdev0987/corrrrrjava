package in.com.collectionself1;

public class P1 {
public static void main(String[] args) {
	
	int dayOfweek = 5;
	switch (dayOfweek) {
	case 1:
		System.out.println("sunday");
		break;
		
	case 2:
		System.out.println("monday");
		break;
	case 3:
		System.out.println("theusday");
		break;
	case 4:
		System.out.println("wednesday");
		break;
	case 5:
		System.out.println("thusday");
		break;

	default:
		break;
	}
	
	
	
	
	
}
}
