package in.com.practice;

public class Rectangle extends Shape {
	
	private int length;
	private int width;
	
	public int getLength() {
		return length;
		
	}
	public void setLength() {
		this.length = length;
	}
	
	public int getWidth() {
		return width;
	}
	
	public void setWidth() {
		this.width = width;
	}
	
	
}
